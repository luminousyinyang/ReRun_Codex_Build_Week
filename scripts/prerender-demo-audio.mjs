import { createHash } from "node:crypto";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env");
try {
  const env = await readFile(envPath, "utf8");
  for (const line of env.split(/\r?\n/)) {
    const match = /^\s*([A-Z0-9_]+)=(.*)\s*$/.exec(line);
    if (match && !process.env[match[1]]) process.env[match[1]] = match[2].replace(/^['"]|['"]$/g, "");
  }
} catch {
  // A shell-provided key is equally valid; do not require a local .env file.
}

if (!process.env.OPENAI_API_KEY) {
  throw new Error("OPENAI_API_KEY is required to prerender demo narration.");
}

// This is deliberately a small, explicit list: it is the fixed no-key demo,
// not a cache of learner-provided notes. Reading the source keeps this script
// dependency-free and usable before a production build exists.
const episodeSource = await readFile(join(root, "lib/episode.ts"), "utf8");
const lines = [];
for (const match of episodeSource.matchAll(/(?:line|simpler|simplerAgain):\s*"([^"\\]*(?:\\.[^"\\]*)*)"/g)) {
  lines.push(match[1].replace(/\\"/g, '"'));
}

const voice = "cedar";
const instructions = "Warm, precise retro science-show host. Speak clearly with light dramatic momentum.";
const uniqueLines = [...new Set(lines.map((line) => line.trim().replace(/\s+/g, " ")))]
  .sort();
const audioDir = join(root, "public/assets/audio");
const entries = {};
await mkdir(audioDir, { recursive: true });

for (const line of uniqueLines) {
  const filename = `${createHash("sha256").update(`${voice}:${line}`).digest("hex")}.mp3`;
  const response = await fetch("https://api.openai.com/v1/audio/speech", {
    method: "POST",
    headers: { "content-type": "application/json", authorization: `Bearer ${process.env.OPENAI_API_KEY}` },
    body: JSON.stringify({ model: process.env.OPENAI_TTS_MODEL ?? "gpt-4o-mini-tts", voice, instructions, input: line, response_format: "mp3" }),
  });
  if (!response.ok) throw new Error(`Could not render demo narration: ${response.status} ${await response.text()}`);
  await writeFile(join(audioDir, filename), Buffer.from(await response.arrayBuffer()));
  entries[`${voice}:${line}`] = filename;
}

const manifest = { version: 1, entries };
await writeFile(join(audioDir, "manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`);
await writeFile(join(root, "lib/demo-audio-manifest.ts"), `/** Generated by npm run prerender:demo-audio. */\nexport const demoAudioManifest = ${JSON.stringify(manifest, null, 2)} as const;\n\nexport function bundledDemoAudioFile(voice: string, line: string) {\n  return demoAudioManifest.entries[\`${"${voice}"}:\${line.trim().replace(/\\s+/g, " ")}\` as keyof typeof demoAudioManifest.entries];\n}\n`);
console.log(`Prerendered ${uniqueLines.length} demo narration files in ${audioDir}.`);
