import { createHash } from "node:crypto";
import { mkdir, readFile, stat, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { createServer } from "vite";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = join(root, ".env");
try {
  const env = await readFile(envPath, "utf8");
  for (const line of env.split(/\r?\n/)) {
    const match = /^\s*([A-Z0-9_]+)=(.*)\s*$/.exec(line);
    if (match && !process.env[match[1]]) process.env[match[1]] = match[2].replace(/^['"]|['"]$/g, "");
  }
} catch {
  // A shell-provided key is equally valid; do not require a local .env file.
}

if (!process.env.OPENAI_API_KEY) {
  throw new Error("OPENAI_API_KEY is required to prerender demo narration.");
}
const ttsModel = process.env.OPENAI_TTS_MODEL?.trim() || "gpt-4o-mini-tts";

// Load the same typed fixtures used by the player through Vite's SSR loader.
// This preserves the app's @ alias and avoids the former fragile source regex.
const vite = await createServer({
  configFile: join(root, "vitest.config.ts"),
  logLevel: "error",
  appType: "custom",
  server: { middlewareMode: true, hmr: false },
});
const { collectDemoNarrationTracks } = await vite.ssrLoadModule("/lib/demo-narration-catalog.ts");
const tracks = collectDemoNarrationTracks();
await vite.close();
const audioDir = join(root, "public/assets/audio");
const entries = {};
await mkdir(audioDir, { recursive: true });

for (const { key, line, voice, showId } of tracks) {
  const filename = `${createHash("sha256").update(key).digest("hex")}.mp3`;
  try {
    if ((await stat(join(audioDir, filename))).size > 0) {
      entries[key] = filename;
      continue;
    }
  } catch {
    // Render missing files below. This makes interrupted batches resumable.
  }
  const response = await fetch("https://api.openai.com/v1/audio/speech", {
    method: "POST",
    headers: { "content-type": "application/json", authorization: `Bearer ${process.env.OPENAI_API_KEY}` },
    body: JSON.stringify({ model: ttsModel, voice, instructions: `Perform as the original ${showId} educational-show host. Keep the delivery clear, supportive, and paced for captions.`, input: line, response_format: "mp3" }),
  });
  if (!response.ok) throw new Error(`Could not render demo narration: ${response.status} ${await response.text()}`);
  await writeFile(join(audioDir, filename), Buffer.from(await response.arrayBuffer()));
  entries[key] = filename;
}

const manifest = { version: 1, entries };
await writeFile(join(audioDir, "manifest.json"), `${JSON.stringify(manifest, null, 2)}\n`);
await writeFile(join(root, "lib/demo-audio-manifest.ts"), `/** Generated by npm run prerender:demo-audio. */\nexport const demoAudioManifest = ${JSON.stringify(manifest, null, 2)} as const;\n\nexport function bundledDemoAudioFile(voice: string, line: string) {\n  return demoAudioManifest.entries[\`${"${voice}"}:\${line.trim().replace(/\\s+/g, " ")}\` as keyof typeof demoAudioManifest.entries];\n}\n`);
console.log(`Prerendered ${tracks.length} demo narration files in ${audioDir}.`);
